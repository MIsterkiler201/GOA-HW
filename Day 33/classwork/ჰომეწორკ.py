# 1. ფუნქცია, რომელიც აბრუნებს რიცხვის კვადრატს
def square_number(num):
    return num ** 2

print(square_number(5))  # 25


# 2. ფუნქცია, რომელიც აბრუნებს ორი რიცხვის ჯამს
def sum_two_numbers(a, b):
    return a + b

print(sum_two_numbers(3, 7))  # 10


# 3. ფუნქცია, რომელიც აბრუნებს სიის ყველაზე პატარა ელემენტს
def find_smallest(numbers):
    return min(numbers)

print(find_smallest([10, 3, 6, 1, 9]))  # 1


# 4. ფუნქცია, რომელიც ამოწმებს, არის თუ არა რიცხვი დადებითი
def is_positive(num):
    return num > 0

print(is_positive(-5))  # False
print(is_positive(7))   # True


# 5. ფუნქცია, რომელიც აბრუნებს სიის ყველა ელემენტის ჯამს
def sum_list(numbers):
    return sum(numbers)

print(sum_list([10, 20, 30, 40, 50]))  # 150
